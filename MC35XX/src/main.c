	  case MC3555:
		  set_do8_hold_reg_addr(0);
		  xTaskCreate( dout8Task, ( const char * ) "DOUT8", configMINIMAL_STACK_SIZE, NULL, DOUT_TASK_PRIORITY, NULL );
