		  break;
	  case MC3552:
		  set_di8_inp_reg_addr(0);
		  set_do8_hold_reg_addr(0);
		  set_di8_set(0);
		  xTaskCreate( din8Task, ( const char * ) "DIN8", configMINIMAL_STACK_SIZE, NULL, DOUT_TASK_PRIORITY, NULL );
		  xTaskCreate( dout8Task, ( const char * ) "DOUT8", configMINIMAL_STACK_SIZE, NULL, DOUT_TASK_PRIORITY, NULL );
		  break;
	  case MC3553:
		  set_di8_inp_reg_addr(16);
		  set_di8_set(1);
